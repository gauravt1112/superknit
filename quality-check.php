we have invested in the best technology:
	</div>
	
	<div class="quality-check-para-two wow fadeIn" data-wow-duration="2s" data-wow-delay="0.4s">
	After hand linking and rosso linking, the lose threads are removed and the seam is analyzed for fineness before the socks are passed on for the next stage of production.<br><br>

Our socks are also checked while they are on the boarding machine. Any socks that are not up to the quality standards are removed. The socks then go for pairing and at this stage are again checked before they are approved to go to the packaging department.<br><br>

The raw materials used in the packing of the socks such as labels, band roles, stickers and hangers are checked with regard to our quality parameters. Here the socks are again checked for all the quality parameters including measurements, color assortments, labeling and barcoding to guarantee the socks are meeting all the customers' specifications and are of the highest quality. <br><br>

After the socks/boxes are packed there is a final random check of 5% of the boxes. This is done by the QC personnel who are trained to act like they represent the buyer. Keeping the buyers interests in mind they have the power to override other employees in the company with regards to the approval of goods before dispatch.
	</div>
	
