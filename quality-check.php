	<div class="quality-check-para-two wow fadeIn" data-wow-duration="2s" data-wow-delay="0.5s">
	During the process of knitting, a sample sock is hung on the machine, utilized as a reference for the other socks. This sample sock has been approved for a specified color, size and design. The technicians in charge, then, ensure every sock manufactured comprises of these exact specifications. The technicians also inspect for the quality of the knitting by  measuring the different dimensions to compare with the original.<br><br>

QC personnel continuously check each machine at least once every four hours with the sock tester machine to confirm the length, width and stretch parameters of the socks.<br><br>

Prior to batch production, a few socks are manufactured on each of the machines to assure the manufactured socks are of identical size, weight, color, design, etc. Upon the head technician's approval, we begin producing them in bulk.
	</div>
	
	<div class="quality-check-para-one wow fadeIn" data-wow-duration="2s" data-wow-delay="0.3s">
