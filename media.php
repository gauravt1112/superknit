			<h3 class="media-name">Supersox to unveil performance - based socks</h3>
			<h4 class="media-heading-date">Sujata Sachdeva | Friday, 07 August 2015</h4>
			<p class="show-content">
Supersox socks brand was launched by Super Knit Industries in 2012. While the company started off as an exporter to Italy and Spain in 2004, by 2012, Naval Saraf, Proprietor of the company decided to venture into the domestic market. It launched a wide collection of socks for men, women and kids of all ages.

Now, the brand is expanding its offerings to auxiliary lines. “We are planning to launch performance based socks, such as blood pressure socks, diabetes socks, ankle joint support socks and knee support socks among other. These are technology based products and would be available in the market within three to four months,” informs Saraf.
			</p>
			
			<span class="read-more">
			
			<p class="hide-content">
				<span style="font-weight: 900;">Expanding into performance-based socks</span><br>
The brand that caters to the entire family with its wide assortment of socks also produces fabrics for the products. This is done by sourcing yarn and knitting the whole socks at company’s manufacturing unit. The fabrics majorly in use are mercerised cotton, combed cotton, cotton, spandex and nylon in more than 100 colours. <br><br>

<span style="font-weight: 900;">Strengthening pan-India footprint</span><br>
The brand is available across the country through a distribution network, of about 115 distributors. Elaborating on retail expansion plans, Saraf says, “We are expanding in every part of the country. The range will be available in 600 MBOs soon. We may also open our own EBOs, about two or three EBOs in the next couple of years, most probably in Mumbai. In terms of revenue, northern and western markets are good for us. We have also partnered some 12 online portals.”<br>

The foremost challenge for a socks brands in India according to Saraf, is that its total volume is small while the budgets are normally high. “Also, since socks are a part of accessories for large apparel retailers, they can easily market their products compared to the standalone socks brands. Moreover, the returns in this segment are not very promising because the size of the market is small,” he exclaims.<br>

After witnessing a 15 percent growth rate last year, Supersox is now targeting to achieve 20 percent growth this year.
